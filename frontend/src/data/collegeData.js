export const normalizeCollegeCode = (value) => {
  const raw = String(value || "").trim();
  if (!raw) return "";
  if (/^\d+$/.test(raw)) return raw.padStart(3, "0");
  return raw;
};

export const getCollegeByCode = (code) => {
  const finalCode = normalizeCollegeCode(code);
  return colleges.find((c) => c.code === finalCode) || null;
};
